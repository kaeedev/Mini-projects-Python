#TIEMPO DE HANNAH NEISE
print('Introduce el tiempo completo de Hannah Neise')
print('Introduce los minutos')
minutosH = input()
print('Introduce los segundos')
segundosH = input()
print('Introduce las centesimas')
centesimasH = input()
#Calculamos los segundos totales
segundosTotalesH = (int(minutosH)*60) + int(segundosH) + (int(centesimasH)/100)
print('El tiempo total de Hannah Neise en segundos es: ' + str(segundosTotalesH) + ' segundos')

print ( ' ')
#Calculamos la velocidad media
velocidadMediaH = 100/float(segundosTotalesH)
print('La velocidad media de Hannah Neise en metros por segundo midiendo 100 metros la pista es: ' + str(velocidadMediaH) + ' metros por segundo')
print ( ' ')


